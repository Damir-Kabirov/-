export default class FilmDetailView {
  constructor(film,comment) {
    this.film = film;
    this.comment = comment;
  }
  getTemplate() {
    return createFilmDetailTemplate(this.film,this.comment);
  }

  getElement() {
    if (!this.element) {
      this.element = createElement(this.getTemplate());
    }

    return this.element;
  }

  removeElement() {
    this.element = null;
  }
}